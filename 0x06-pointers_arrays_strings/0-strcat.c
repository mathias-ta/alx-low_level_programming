#include <stdio.h>

/**
 *_strcat - concatenates two strings
 *@dest: string 1
 *@src: string 2
 *Return: concatenated string
 */

char *_strcat(char *dest, char *src)
{

char *concstr;
int i = 0;
int j = 0;

while (dest[i] != '\0')
{
concstr[i] = dest[i];
i++;
}
while (src[j] != '\0')
{
concstr[i] = src[j];
j++;
i++;
}
concstr[i] = '\0';

return (concstr);


}
